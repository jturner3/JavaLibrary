package _09_loops;

public class _01_while {
	public static void main(String[] args) {
		//Count from 0 to 10
		
		int count = 0;
		while(count <= 10){
			System.out.println(count);
			count++;
		}
		
		//Count from 10 to zero
		
		int countTwo = 10;
		while(countTwo >= 0){
			System.out.println(countTwo);
			countTwo --;
		}
		
		//From 100 count bakcwards by 5s
		
		int countThree = 100;
		while(countThree >=0){
			System.out.println(countThree);
			countThree -= 5 ;
		}
		
		
		
		
		
		
	}
		
}


